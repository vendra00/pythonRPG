class Character:
    def __init__(self, name, health, max_health, attack, defense, level, attributes, race, character_class, experience):
        self.name = name
        self.health = health
        self.max_health = max_health
        self.attack = attack
        self.defense = defense
        self.level = level
        self.attributes = attributes
        self.race = race
        self.character_class = character_class
        self.experience = experience
